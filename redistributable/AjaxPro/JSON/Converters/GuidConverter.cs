using System;
using System.Text;
using System.Data;

namespace AjaxPro
{
	/// <summary>
	/// Provides methods to serialize and deserialize a Guid object.
	/// </summary>
	public class GuidConverter : IJavaScriptConverter
	{
        /// <summary>
        /// Initializes a new instance of the <see cref="GuidConverter"/> class.
        /// </summary>
		public GuidConverter()
			: base()
		{
			m_serializableTypes = new Type[] {
				typeof(Guid)
			};
			m_deserializableTypes = m_serializableTypes;
		}

        /// <summary>
        /// Converts a .NET object into a JSON string.
        /// </summary>
        /// <param name="o">The object to convert.</param>
        /// <returns>Returns a JSON string.</returns>
		public override string Serialize(object o)
		{
			StringBuilder sb = new StringBuilder();
			Serialize(o, sb);
			return sb.ToString();
		}

        /// <summary>
        /// Serializes the specified o.
        /// </summary>
        /// <param name="o">The o.</param>
        /// <param name="sb">The sb.</param>
		public override void Serialize(object o, StringBuilder sb)
		{
			// Guids are not supported by JavaScript, we will return the
			// string representation using following format:
			// xxxxxxxx-xxxx-xxxx-xxxx-xxxxxxxxxxxx 

			JavaScriptUtil.QuoteString(o.ToString(), sb);
		}

        /// <summary>
        /// Converts an IJavaScriptObject into an NET object.
        /// </summary>
        /// <param name="o">The IJavaScriptObject object to convert.</param>
        /// <param name="t"></param>
        /// <returns>Returns a .NET object.</returns>
		public override object Deserialize(IJavaScriptObject o, Type t)
		{
			return new Guid(o.ToString());
		}
	}
}
